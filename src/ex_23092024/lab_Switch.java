package ex_23092024;

public class lab_Switch {
    public static void main(String[] args) {
        int a=10;
        switch (a)
        {
            case 1:
                System.out.println(1);
                break;
            case 2:
                System.out.println(2);
                break;
            default:
                System.out.println(10);
        }
        System.out.println("End the Switch loop !!");
    }
}
