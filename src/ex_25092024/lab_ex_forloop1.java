package ex_25092024;

public class lab_ex_forloop1 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) { //out put 0 to 9
            System.out.println(i);
        }
        System.out.println("---------------");
        for (int i = 0; i <= 10; i++) { //output 0 to 10
            System.out.println(i);
        }
    }
}
